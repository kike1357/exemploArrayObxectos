public class Xogador {
    private String nome;
    private int dorsal;

    public Xogador(String nome, int dorsal) {
        this.nome = nome;
        this.dorsal = dorsal;
    }


    public String getNome() {
        return nome;
    }


    public int getDorsal() {
        return dorsal;
    }

    public void setDorsal(int dor) {
        this.dorsal = dor;
    }
    }
        
    

    
    

